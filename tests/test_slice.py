def test_scad_export_failure_exits_as_gate_failure(
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    import commands.slice as slice_cmd

    model = tmp_path / "part.scad"
    model.write_text("cube(1);\n")
    monkeypatch.setenv("SLICER", str(_fake_slicer(tmp_path)))
    monkeypatch.setattr(slice_cmd, "_prepare_export", lambda _inp, _out, _defs: 1)

    with pytest.raises(GateFailure) as got:
        slice_cmd.run([str(model), "--dry-run"])

    assert got.value.exit_code == 1
    assert "STL export failed" in got.value.message


def test_profile_list_is_stripped_before_invoking_slicer(
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    import json

    import commands.slice as slice_cmd

    model = tmp_path / "part.stl"
    machine = tmp_path / "machine.json"
    process = tmp_path / "process.json"
    arg_log = tmp_path / "args.json"
    model.write_text("solid part\nendsolid part\n")
    machine.write_text("{}")
    process.write_text("{}")
    monkeypatch.setenv("SLICER", str(_fake_slicer(tmp_path)))
    monkeypatch.setenv("ARG_LOG", str(arg_log))

    assert slice_cmd.run([str(model), "--dry-run", "--profile", f"{machine}, {process}"]) == 0

    args = json.loads(arg_log.read_text())
    assert str(machine) in args
    assert str(process) in args
    assert f" {process}" not in args
