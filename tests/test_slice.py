def _fake_slicer(tmp_path: Path) -> Path:
    slicer = tmp_path / "fake_slicer.py"
    slicer.write_text(
        "#!/usr/bin/env python3\n"
        "import json\n"
        "import os\n"
        "from pathlib import Path\n"
        "import sys\n"
        "args = sys.argv[1:]\n"
        "arg_log = os.environ.get('ARG_LOG')\n"
        "if arg_log:\n"
        "    Path(arg_log).write_text(json.dumps(args))\n"
        "if '--output' in args:\n"
        "    out = Path(args[args.index('--output') + 1])\n"
        "elif '--outputdir' in args:\n"
        "    out = Path(args[args.index('--outputdir') + 1]) / 'fake.gcode'\n"
        "else:\n"
        "    raise SystemExit(2)\n"
        "out.parent.mkdir(parents=True, exist_ok=True)\n"
        "out.write_text('; generated by fake slicer\\n')\n"
        "print('estimated printing time: 1h 02m')\n"
        "print('filament used: 3.4g')\n"
    )
    slicer.chmod(0o755)
    return slicer


