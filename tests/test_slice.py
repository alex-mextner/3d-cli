from __future__ import annotations

from pathlib import Path

import pytest

from errors import GateFailure, InvalidArgument


def _fake_slicer(tmp_path: Path) -> Path:
    slicer = tmp_path / "fake_slicer.py"
    slicer.write_text(
        "#!/usr/bin/env python3\n"
        "import json\n"
        "import os\n"
        "from pathlib import Path\n"
        "import sys\n"
        "args = sys.argv[1:]\n"
        "arg_log = os.environ.get('ARG_LOG')\n"
        "if arg_log:\n"
        "    Path(arg_log).write_text(json.dumps(args))\n"
        "if '--output' in args:\n"
        "    out = Path(args[args.index('--output') + 1])\n"
        "elif '--outputdir' in args:\n"
        "    out = Path(args[args.index('--outputdir') + 1]) / 'fake.gcode'\n"
        "else:\n"
        "    raise SystemExit(2)\n"
        "out.parent.mkdir(parents=True, exist_ok=True)\n"
        "out.write_text('; generated by fake slicer\\n')\n"
        "print('estimated printing time: 1h 02m')\n"
        "print('filament used: 3.4g')\n"
    )
    slicer.chmod(0o755)
    return slicer


def test_help_explains_dry_run_and_profiles(capsys: pytest.CaptureFixture[str]) -> None:
    import commands.slice as slice_cmd

    assert slice_cmd.run(["--help"]) == 0

    out = capsys.readouterr().out
    assert "--dry-run" in out
    assert "--check" in out and "deprecated" in out
    assert "--list-profiles" in out
    assert "machine" in out
    assert "process" in out
    assert "filament" in out
    assert "export config" in out


def test_dry_run_discards_gcode(
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
    capsys: pytest.CaptureFixture[str],
) -> None:
    import commands.slice as slice_cmd

    model = tmp_path / "part.stl"
    model.write_text("solid part\nendsolid part\n")
    final_out = tmp_path / "part.gcode"
    monkeypatch.setenv("SLICER", str(_fake_slicer(tmp_path)))

    assert slice_cmd.run([str(model), "--dry-run", "-o", str(final_out)]) == 0

    captured = capsys.readouterr()
    assert "STATUS: PASS" in captured.out
    assert "dry-run" in captured.out
    assert "discarded" in captured.out
    assert not final_out.exists()


