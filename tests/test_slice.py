def test_dry_run_discards_gcode(
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
    capsys: pytest.CaptureFixture[str],
) -> None:
    import commands.slice as slice_cmd

    model = tmp_path / "part.stl"
    model.write_text("solid part\nendsolid part\n")
    final_out = tmp_path / "part.gcode"
    monkeypatch.setenv("SLICER", str(_fake_slicer(tmp_path)))

    assert slice_cmd.run([str(model), "--dry-run", "-o", str(final_out)]) == 0

    captured = capsys.readouterr()
    assert "STATUS: PASS" in captured.out
    assert "dry-run" in captured.out
    assert "discarded" in captured.out
    assert not final_out.exists()


